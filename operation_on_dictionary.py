# Dictionary operations
my_dict = {'name': 'John', 'age': 25, 'city': 'New York'}

# Accessing values
print(f"Name: {my_dict['name']}, Age: {my_dict['age']}, City: {my_dict['city']}")

# Adding a new key-value pair
my_dict['gender'] = 'Male'

# Updating a value
my_dict['age'] = 26

# Deleting a key
del my_dict['city']

print("Updated Dictionary:", my_dict)